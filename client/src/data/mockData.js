export const savePerson = (person) => {
  const people = loadPeople();
  if (person.id) {
    // Edit existing person
    const index = people.findIndex(p => p.id === person.id);
    if (index !== -1) {
      people[index] = person;
    }
  } else {
    // Add new person
    person.id = Math.max(...people.map(p => p.id), 0) + 1;
    person.dateAdmitted = new Date().toISOString().split('T')[0];
    people.push(person);
  }
  localStorage.setItem('cheshire-people', JSON.stringify(people));
  return person;
};

export const deletePerson = (id) => {
  const people = loadPeople();
  const filtered = people.filter(p => p.id !== id);
  localStorage.setItem('cheshire-people', JSON.stringify(filtered));
};